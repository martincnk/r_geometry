surface_square <- function(a) {
  b <- a ^ 2
}
surface_paraleograme <- function(a, b) {
  a * b
}

surface_triangle <- function(a, b) {
  a * b / 2
}
surface_regular_polygone <- function(a, b, c) {
  a * b * c
}
surface_trapeze <- function(a, b, c) {
  ((a + b) * c) / 2
}
surface_diamond <- function(a, b) {
  a * b / 2
}
